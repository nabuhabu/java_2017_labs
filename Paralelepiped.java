public class Paralelepiped {
	
		    private int square;
		    private int volume;

		    public int getSquare() {
		        return square;
		    }
		   
		    public void setSquare(int square) {
		        this.square = square;
		    }

		    public int getVolume() {
		        return volume;
		    }

		    public void setVolume(int volume) {
		        this.volume = volume;
		    }
		    
		    
		    public Paralelepiped(int square, int volume)
		    {
		    	this.square = square;
		    	this.volume = volume;
		    }
		    
		    

}
